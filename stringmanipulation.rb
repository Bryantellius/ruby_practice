# .downcase converts characters to lowercase
# .upcase converts characters to uppercase
# .capitalize capitalizes first letter of string
# .reverse reverses string
# .swapcase toggles upper and lower cases for each letter
# .length returns string length
# .chomp removes trailing spaces

puts "Enter name:"
name = gets

puts ".downcase = #{name.downcase}"

puts ".upcase = #{name.upcase}"

puts ".capitalize = #{name.capitalize}"

puts ".reverse = #{name.reverse}"

puts ".swapcase = #{name.swapcase}"

puts ".chomp = #{name.chomp}"

puts ".length = #{name.length}"